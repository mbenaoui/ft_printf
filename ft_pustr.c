#include "ft_printf.h"

int	ft_pustr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		i = ft_pustr("(null)");
		return (i);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
